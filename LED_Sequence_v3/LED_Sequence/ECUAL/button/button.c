EN_return button_init(EN_ports port, EN_pins pin)
{
	if(port < porta || port > portd || pin < pin0 || pin > pin7){
		return NOT_OK;
	}
	DIO_init(port,pin,INPUT);
	return OK;
}
EN_return button_read(EN_ports port,EN_pins pin, EN_state* state)
{
	if(port < porta || port > portd || pin < pin0 || pin > pin7){
		return NOT_OK;
	}
	DIO_read(port,pin,state);
	return OK;
}


